                    <h2 class="subchapter-title">Trabalho produtivo e reprodutivo no cotidiano das mulheres brasileiras</h2>

                    <p class="first-p long-text">A vida cotidiana muitas vezes descrita como restrita à vida privada, e/ou como relativa apenas ao corriqueiro e ao que se repete está em oposição a uma noção de vida cotidiana como uma dimensão da vida social construída historicamente e marcada pelas estruturas e relações sociais.</p>
                    <p>A pesquisa que toma como ponto de partida o cotidiano pode revelar a inextricabilidade entre as várias esferas sociais, em geral tratadas como autônomas, ou fragmentadas, como é o caso da relação entre trabalho remunerado e trabalho doméstico gratuito, entre o lazer, o trabalho, o descanso e o desenvolvimento pessoal.</p>
                    <p>A pesquisa sobre vida cotidiana, de acordo com Lefebvre, foi um método para se estabelecer analiticamente um recorte da realidade social, que se constrói como possibilidade sociológica a partir do século XIX, quando o “centro da reflexão se desloca; abandona a especulação para acercar-se da realidade empírica e prática, dos dados e da consciência” (LEFEBVRE, 1972, p. 21).</p>
                    <p>No sistema social capitalista e patriarcal, o trabalho produtivo é uma dimensão central na organização da vida cotidiana. Este trabalho é a base a partir da qual as/os trabalhadoras/es, a grande maioria da população, têm acesso aos recursos e aos meios para viver. Na hierarquia que rege a organização do tempo social o tempo do trabalho produtivo e remunerado tem precedência sobre os outros tempos. São as mulheres, nessa forma de organização social, marcada pela divisão sexual do trabalho, que enfrentam, em geral, as tensões e os conflitos gerados pela dinâmica e as exigências do trabalho produtivo e do trabalho reprodutivo.</p>
                    <p>A divisão sexual do trabalho tem como princípios organizadores a hierarquização e separação entre trabalho produtivo/ homens e trabalho reprodutivo/mulheres,  princípios esses que segundo Kergoat  “se encontram em todas as sociedades conhecidas e são legitimados pela ideologia naturalista”.  A autora acrescenta que isso isso não quer dizer “...que a divisão sexual do trabalho se ja um dado imutável. Ao contrário, essas modalidades concretas variam fortemente no tempo e no espaço, como o demonstraram abundantemente etnólogos/as e historiadores/as” (KERGOAT, 2001, p. 89).</p>
                    <p>As desigualdades entre homens e mulheres a partir de uma análise que considera o trabalho produtivo e o trabalho reprodutivo revelaram
                    <p>... a necessidade de contemplar um cenário mais amplo do que o mercado de trabalho e a família: a vida cotidiana. Um território analítico no qual tem sido possível delimitar as presenças e as ausências masculinas e femininas, de maneira estrita, e reconhecer a divisão sexual e hierárquica que as preside  (TORNS, 2002, p. 135).</p>
                    <p>A pesquisa realizada pelo Datapopular, SOS Corpo e Instituto Patrícia Galvão, buscou justamente conhecer como as mulheres brasileiras enfrentam as demandas do trabalho produtivo e reprodutivo - tensões, dificuldades, arranjos e demandas – tomando como base as percepções e descrições das mulheres de suas dinâmicas de trabalho no cotidiano. Baseada em uma metodologia qualitativa e quantitativa, entre os meses de março e julho de 2012, foram realizados 08 grupos de discussão com mulheres e homens, em São Paulo e Recife; em seguida, foram entrevistadas 800 trabalhadoras (18 aos 64 anos) de regiões metropolitanas de 08 estado s (PE, CE, BA, SP, RJ, MG, RS, PA e DF).</p>
                    <p>As alterações ocorridas no mundo do trabalho, como demonstra a pesquisa, não levaram a mudanças significativas na divisão sexual do trabalho. O que se observa é que essa divisão do trabalho permanece, produzindo consequências que afetam diretamente as mulheres, que continuam como as principais responsáveis pelos afazeres domésticos e cuidados com filhos. Falta de tempo e grande sobrecarga marcam seu cotidiano. Os homens e  o Estado, segundo os resultados da pesquisa aqui apresentada, pouco contribuem para a mediação das jornadas. Segundo a pesquisa, na percepção das mulheres brasileiras, as demandas principais para a atua ção do poder público para o enfrentamento das tensões geradas pela dupla jornada de trabalho no cotidiano são creches, transporte público de qualidade e escolas em tempo integral.</p>
                    <p>A realização desta pesquisa tem como finalidade contribuir  para subsidiar os movimentos de mulheres em suas lutas pela superação das desigualdades no mundo do trabalho, como também para oferecer subsídios ao poder público na elaboração de políticas que respondam a essas demandas. E, ainda, é uma contribuição ao debate e ao desenvolvimento das pesquisas e estudos no campo do trabalho e, em particular, da relação entre trabalho produtivo e trabalho reprodutivo.</p>
                    <p>Esta pesquisa foi realizada no âmbito de um projeto coletivo, formado por sete organizações feministas – SOS Corpo Instituto Feminista para a Democracia, Centro Feminista de Estudos e Assessoria – Cfemea, Coletivo Leila Diniz, Rede de Desenvolvimento Humano – Redeh, Cunhã Coletivo Feminista, Geledés Instituto da Mulher Negra e Instituto Patrícia Galvão – , com apoio do Fundo para a Igualdade de Gênero da ONU Mulheres.</p>

