                    <h2 class="subchapter-title">Brasil de verdade, mulher de verdade</h2>

                    <p class="first-p">Diante de um histórico de lutas promovidas por diversos movimentos feministas brasileiros, são inegáveis as conquistas das mulheres nas últimas décadas. Entre elas, o ingresso de um grande contingente feminino no mercado de trabalho e a leve tendência de redução da diferença de salários. Mas a tão almejada situação de igualdade de gêneros ainda está longe de ser atingida. Há um caminho extenso a ser percorrido.</p>
                    <p>Nesta pesquisa, constatamos em números o que já sabíamos na prática, nas conversas do dia a dia com centenas de mulheres do nosso convívio. Sim, as mulheres ainda acumulam mais funções cotidianas que os homens. Além de trabalhar fora, têm que tomar para si, muitas vezes integralmente, a responsabilidade dos cuidados com a casa e com os filhos. Embora a nova geração de mulheres seja mais escolarizada e tenhamos identificado a ampliação do número de internautas, o que ajuda a trazer mais informação para aquelas de classes mais baixas, a maior parte delas continua atarefada e cada vez mais sobrecarregada. E esse acúmulo de atividades está atrelado ao fardo da cultura machista que elas têm que lidar em sua realidade.</p>
                    <p>Tive a oportunidade de conversar com várias destas mulheres. Não foi difícil identificar mulheres que ainda preservam a alegria de viver, mesmo tendo de acordar muito cedo, preparar as refeições para a família e enfrentar transportes públicos abarrotados para chegar ao trabalho. E dentro deste caos diário têm que se desdobrar para arrumar quem tome conta dos filhos, sendo que as creches quase sempre são escassas ou fecham antes que consigam retornar para casa. Na volta, à noite, mais uma vez passam pelos inconvenientes das conduções lotadas, que trafegam lentamente pelas vias de grandes cidades, tornando a jornada ainda mais exaustiva. E depois de passar por tudo isso, mal chegam em casa e precisam retomar as atividades domésticas, como limpar a casa, lavar roupa e preparar o jantar. Quase sempre sem ajuda de maridos e filhos.</p>
                    <p>Essa é uma realidade frequente na vida das mulheres que precisa ser mudada. Entretanto, não podemos nos esquecer de seus ganhos neste longo período de lutas. Nos últimos anos, a renda delas tem crescido mais do que a dos homens. Isso faz toda a diferença. Uma renda maior possibilitou voz ativa e autonomia para tomar decisões que antes ficavam apenas a cargo dos maridos. Em outros casos, o aumento do número de mulheres na condição de chefes de família tornou-as socialmente mais influentes, embora a grande maioria em condições socioeconômicas desfavoráveis. Porém, com a ida ao mercado de trabalho e com  renda própria, ela às vezes encontra a possibilidade, inclusive, de se desprender de um c asamento no qual não se realiza.</p>
                    <p>Ao confrontarmos o espaço conquistado pelas mulheres e as necessidades ainda existentes para atingirmos uma sociedade justa, concluímos que no Brasil de Verdade, palco da classe média que já desponta, devemos intensificar o debate por políticas públicas e por mudanças culturais que promovam a igualdade de gênero.</p>
                    <span class="text-author">Renato Meirelles</span>
                    <span class="text-author-title">Sócio-diretor do Data Popular</span>
